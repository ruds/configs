<p><strong>CC Mode is an Emacs and XEmacs mode for editing C and other
languages with similar syntax; currently C++, Objective-C, Java,
CORBA's IDL, Pike, and AWK. It's a standard package in both Emacs and
XEmacs.</strong>

<p>CC Mode is maintained by Alan Mackenzie, with numerous
contributions from other people. Several other people have helped make
CC Mode what it is today, notably Barry Warsaw who took over BOCM
(Boring Old C Mode) back in 1992 and revamped it to CC Mode, and
Martin Stjernholm, CC Mode's maintainer from 2000 to 2005.

<p>The current release available for <a
href="release.php">download</a> is 5.32.3.  There is a detailed list
of <a href="changes-532.php">user visible changes</a>, as well as <a
href="installation.php">installation instructions</a> and Emacs/XEmacs
<a href="compat.php">compatibility details</a>.

<p>When a patch release is made, the last number in its version spec
(e.g. 5.32.3) is the patch release number.  Patch releases are made fairly
frequently and contain only bug fixes since the last "real" release.  The <a
href="src/ChangeLog">ChangeLog file</a> lists the bugs fixed.

<p>You can <a href="html-manual/index.html">browse the documentation
on-line</a>.  It's also available for <a href="manual/">download</a>
in several formats.

<p>For bug reports, feature requests, kudos, and anything else
concerning CC Mode, please mail <a
href="mailto:bug-cc-mode@gnu.org">bug-cc-mode@gnu.org</a>.  There is
also an announcement only <a href="lists.php">mailing list</a> you can
join to get notified of new CC Mode releases.

<p>If you want the latest bug fixes or feel adventurous and want to contribute
(don't hesitate! ;), clone the
<a href="hgaccess.php">source repository</a>.

<p>See also <a href="contrib.php">some related Emacs packages</a> that
have been contributed by CC Mode users.

